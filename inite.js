function uimake(targetSq) {
  if (!excl(targetSq) && targetSq !== undefined) {
    var move = gameState.selectedMoves.find(m => m.to === targetSq);
    if (move) {
      // auto-promote 4 now
      if (move.type === 'promo')
        move = gameState.selectedMoves.find(m => m.to===targetSq && m.promo==='queen') || move;
      makeMove(move);
      return true;
    }
  }
}

