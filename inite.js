// how do you find which square the mouse is on?
// the shape can be broken up into three pieces,
// the left circle, the middle diamond, and the right circle.
// first check if mouse is in the middle diamond,
// this is true if the mouse x and y solve these 4 inequalities:
// y < x + 0.21213203
// y < 1.21213203 - x
// y > x - 0.21213203
// y > 0.78786797 - x
// otherwise, we can do further left circle checks if x < 0.5
// or else check right circle.
// once we know the region the mouse is in it will be much easier to figure out
// which cell exactly it is in, if any.

function updateMouse(event) {
  var rect = canvas.getBoundingClientRect();
  var cx = event.clientX - rect.left;
  var cy = event.clientY - rect.top;
  var x = cx / s;
  var y = cy / s;
  mouse.cx = cx;
  mouse.cy = cy;
  mouse.x = x;
  mouse.y = y;
  if (
    y < x + 0.21213203 &&
    y < 1.21213203 - x &&
    y > x - 0.21213203 &&
    y > 0.78786797 - x
  ) mouse.id = getIdDiamond(x,y);
  else if (x < 0.5) mouse.id = getIdLCircle(x,y);
  else mouse.id = getIdRCircle(x,y);
}

