var root005 = Math.sqrt(.005);

function getIdDiamond(x,y) {
  var u = x + y;
  var v = y - x;
  var i = Math.floor((u - 1)/root005 + 3);
  var j = Math.floor(v/root005 + 3);
  return [
    [-1,71,53,35,17,-1],
    [6,7,8,9,10,11],
    [24,25,26,27,28,29],
    [42,43,44,45,46,47],
    [60,61,62,63,64,65],
    [-1,54,36,18,0,-1]
  ][j][i];
}

function getIdLCircle(x,y) {
  var fx = F[0][0];
  var fy = F[0][1];
  var r = 20*Math.hypot(x-fx,y-fy)-1;
  if (r < -.5 || r >= 4.5) return -1;
  if (r < 0) r = 0;
  if (r >= 4) r = 3;
  r = Math.floor(r);
  var theta = Math.atan2(y-fy,x-fx);
  var t = Math.floor(((theta/Math.PI-.125+2)%2)*4);
  return [
    [54,55,56,57,58,59,60],
    [36,37,38,39,40,41,42],
    [18,19,20,21,22,23,24],
    [0,1,2,3,4,5,6],
  ][r][t];
}

